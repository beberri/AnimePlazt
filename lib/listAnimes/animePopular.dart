class anime {

  int id;
  int stars;
  String nombre;
  String img;
  String genero;
  String des;

  anime(this.id,this.stars, this.nombre, this.img, this.genero, this.des){
    stars = this.stars;
    id= this.id;
    nombre = this.nombre;
    img = this.img;
    genero = this.genero;
    des = this.des;
  }


}
